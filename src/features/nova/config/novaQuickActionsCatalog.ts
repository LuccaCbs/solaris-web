export function isNovaQuickActionAvailable(
    action: NovaQuickActionDefinition,
    hasModule: (moduleCode: ModuleCode) => boolean,
    hasMinimumRole: (minimumRole: OrganizationRole) => boolean,
): boolean {
    if (action.requiredModule && !hasModule(action.requiredModule)) {
        return false
    }

    if (action.minimumRole && !hasMinimumRole(action.minimumRole)) {
        return false
    }

    return true
}
